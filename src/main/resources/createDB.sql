DROP TABLE IF EXISTS ARTIST;
DROP TABLE IF EXISTS CONCERT;

CREATE TABLE ARTIST(ID INT PRIMARY KEY, NAME VARCHAR(255), GENRE VARCHAR(255));
CREATE TABLE CONCERT(ID INT PRIMARY KEY AUTO_INCREMENT NOT NULL, NAME VARCHAR(255), CDATE DATE, PRICE INT, PLACE VARCHAR(255), DESCRIPTION VARCHAR(1000), TICKETSTOTAL INT NOT NULL, TICKETSSOLD INT, ARTIST_ID INT, FOREIGN KEY (ARTIST_ID) REFERENCES public.ARTIST(ID));

